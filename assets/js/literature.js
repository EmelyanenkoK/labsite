function update_literature() {
  var start_year = $("#start-year")[0].value;
  var end_year = $("#end-year")[0].value;
  if(start_year<2000) {start_year=0;}
  if(end_year<2000) {end_year=2999;}
  var material_divs = $(".literature-checkbox1").toArray();
  var materials = [];
  for(d of material_divs) {
    if(d.childNodes[0].checked) {
      materials.push(d.childNodes[0].id);
    }
  }
  var topic_divs = $(".literature-checkbox2").toArray();
  var topics = [];
  for(d of topic_divs) {
    if(d.childNodes[0].checked) {
      topics.push(d.childNodes[0].id);
    }
  }
  var new_filters = {year_start:start_year, year_finish:end_year, materials:materials, topics:topics};
  if(!(JSON.stringify(new_filters) == JSON.stringify(filters))) {
    window.filters = new_filters;
    fill_literature();
    update_chart();
    if(materials.length <1) {
      $("#checkbox-helper1")[0].innerHTML = "материал не выбран, показаны все";
    } else {
      $("#checkbox-helper1")[0].innerHTML = "";
    }
    if(topics.length <1) {
      $("#checkbox-helper2")[0].innerHTML = "топик не выбран, показаны все";
    } else {
      $("#checkbox-helper2")[0].innerHTML = "";
    }
  }
}


