var filters = {year_start:0, year_finish:3000, materials:[], topics:[]};
function select_literature() {
  var ret = [];
  for(e of literature) {
    if(e['year']<filters.year_start) {continue;}
    if(e['year']>filters.year_end) {continue;}
    if(filters.materials.length > 0) {
      var suitable = true;
      for (t of filters.materials) {
        if(e['tags'].indexOf(t)<0) {
          suitable = false;
          break;
        }
      }
      if(!suitable) {continue;}
    }
    if(filters.topics.length > 0) {
      var suitable = true;
      for (t of filters.topics) {
        if(e['tags'].indexOf(t)<0) {
          suitable = false;
          break;
        }
      }
      if(!suitable) {continue;}
    }
    ret.push(e);
  }
  console.log(ret.length, filters)
  return ret;
}

function fill_literature() {
  var place = $("#literature")[0];
  place.innerHTML="";
  var s = "";
  var suitable_literature = select_literature();
  for(e of suitable_literature) {
    s+= `<li><a href = "${e['link']}"> ${e['title']}</a> 
             <p class="journal">${e['journal']} <strong>${e['year']}</strong></p>
             <p class="authors">${e['authors']}</p>
         </li>
         `
  }
  place.innerHTML=s;
}

function update_literature() {
  var start_year = $("#start-year")[0].value;
  var end_year = $("#end-year")[0].value;
  if(start_year<2000) {start_year=0;}
  if(end_year<2000) {end_year=2999;}
  var material_divs = $(".literature-checkbox1").toArray();
  var materials = [];
  for(d of material_divs) {
    if(d.childNodes[0].checked) {
      materials.push(d.childNodes[0].id);
    }
  }
  var topic_divs = $(".literature-checkbox2").toArray();
  var topics = [];
  for(d of topic_divs) {
    if(d.childNodes[0].checked) {
      topics.push(d.childNodes[0].id);
    }
  }
  var new_filters = {year_start:start_year, year_finish:end_year, materials:materials, topics:topics};
  if(!(JSON.stringify(new_filters) == JSON.stringify(filters))) {
    window.filters = new_filters;
    fill_literature();
    if(materials.length <1) {
      $("#checkbox-helper1")[0].innerHTML = "материал не выбран, показаны все";
    } else {
      $("#checkbox-helper1")[0].innerHTML = "";
    }
    if(topics.length <1) {
      $("#checkbox-helper2")[0].innerHTML = "топик не выбран, показаны все";
    } else {
      $("#checkbox-helper2")[0].innerHTML = "топик не выбран, показаны все";
    }
  }
  fill_literature();
}

